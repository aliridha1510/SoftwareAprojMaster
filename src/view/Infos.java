package view;

import java.sql.Date;

public class Infos {
	 int idFiled;
	 Date dateField;
	 int actualField;
	 float targetField;
	public int getIdFiled() {
		return idFiled;
	}
	public void setIdFiled(int idFiled) {
		this.idFiled = idFiled;
	}
	public Date getDateField() {
		return dateField;
	}
	public void setDateField(Date dateField) {
		this.dateField = dateField;
	}
	public int getActualField() {
		return actualField;
	}
	public void setActualField(int actualField) {
		this.actualField = actualField;
	}
	public float getTargetField() {
		return targetField;
	}
	public void setTargetField(float targetField) {
		this.targetField = targetField;
	}
	 

}
